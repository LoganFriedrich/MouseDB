def fallback_ordered_hemisphere_columns(
    prior: RegionPrior,
    available: Optional[List[str]] = None,
    hemispheres: Tuple[str, ...] = HEMISPHERES,
) -> List[str]:
    """Build ``{region}_{hemisphere}`` column names in prior order.

    Mirrors ``mousedb.region_priors.ordered_hemisphere_columns`` so it can
    stand in when mousedb is not installed.
    """
    cols = [f"{r}_{h}" for r in prior.ordered_regions for h in hemispheres]        # nested list comprehension: outer over regions (priority order), inner over hemispheres -> "Region_both", "Region_left", "Region_right"
    if available is not None:                                                      # caller passed an availability filter (typically the actual columns of the wide matrix)
        avail = set(available)                                                     # convert to set for O(1) membership checks
        cols = [c for c in cols if c in avail]                                     # keep only column names actually present in the data
    return cols
