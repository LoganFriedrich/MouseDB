# All paths resolve relative to this file so the tool is location-independent.
# Copy the folder to any disk, any OS, any directory depth; paths still work.

PACKAGE_DIR = Path(__file__).resolve().parent                  # endpoint_ck_analysis/endpoint_ck_analysis/  -- __file__ is this file's path; .resolve() makes it absolute; .parent strips the filename
PROJECT_ROOT = PACKAGE_DIR.parent                              # endpoint_ck_analysis/ (outer tool folder) -- one level up from the package
BUNDLED_DATA_DIR = PACKAGE_DIR / "_bundled_data"               # Ships with the tool, git-ignored contents -- '/' joins paths cleanly across OSes
BUNDLED_DB_PATH = BUNDLED_DATA_DIR / "connectome.db"           # Default DB location for bundled distributions
CACHE_DIR = BUNDLED_DATA_DIR / "cache"                         # Parquet cache for the six base dataframes (regenerated by 00_setup)
