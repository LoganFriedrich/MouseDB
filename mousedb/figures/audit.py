# PNG compliance checks
# ============================================================================

def audit_png(png_path: Path, registered_paths: set) -> Dict:
    """Audit a single PNG figure for compliance.

    Parameters
    ----------
    png_path : Path
        Path to the PNG file.
    registered_paths : set
        Set of file paths (relative to MOUSEDB_ROOT/figures/) that are
        registered in FigureRegistry.

    Returns a dict with compliance flags.
    """
    result = {
        "path": str(png_path),
        "name": png_path.name,
        "size_kb": round(png_path.stat().st_size / 1024, 1),
    }

    # Check for JSON sidecar
    sidecar_path = png_path.with_suffix(".json")
    result["has_sidecar"] = sidecar_path.exists()

    # Parse sidecar metadata if it exists
    if result["has_sidecar"]:
        try:
            with open(sidecar_path) as f:
                meta = json.load(f)
            result["sidecar_script"] = meta.get("script", "")
            result["sidecar_has_recipe"] = meta.get("script", "").startswith("recipe:")
            result["sidecar_data_sources"] = len(meta.get("data_sources", []))
            result["sidecar_mode"] = meta.get("mode", "")
        except (json.JSONDecodeError, OSError):
            result["sidecar_script"] = ""
            result["sidecar_has_recipe"] = False
            result["sidecar_data_sources"] = 0
            result["sidecar_mode"] = ""
    else:
        result["sidecar_script"] = ""
        result["sidecar_has_recipe"] = False
        result["sidecar_data_sources"] = 0
        result["sidecar_mode"] = ""

    # Check if registered in FigureRegistry
    try:
        rel_path = str(png_path.relative_to(MOUSEDB_ROOT / "figures"))
    except ValueError:
        rel_path = str(png_path)
    result["is_registered"] = rel_path in registered_paths

    return result


