"""
Figure compliance audit tool.

Scans all figure generation scripts and generated PNGs to check compliance
with the 49 figure production rules, FigureRegistry tracking, and the
FigureRecipe system.

Usage:
    from mousedb.figures.audit import run_audit
    report = run_audit()

CLI:
    mousedb figures audit
    mousedb figures backfill
"""

import ast
import json
import re
from pathlib import Path
from typing import Dict, List, Optional, Tuple

from mousedb import MOUSEDB_ROOT, DEFAULT_FIGURES_PATH


# ============================================================================
# Script compliance checks
# ============================================================================

# Patterns we look for in source files to assess compliance
COMPLIANCE_PATTERNS = {
    "imports_mousedb_figures": [
        r"from\s+mousedb\.figures",
        r"import\s+mousedb\.figures",
    ],
    "uses_save_figure": [
        r"save_figure\s*\(",
    ],
    "uses_register": [
        r"register\s*=\s*True",
        r"registry\.register\s*\(",
        r"FigureRegistry",
    ],
    "uses_figure_legend": [
        r"FigureLegend\s*\(",
    ],
    "uses_methodology_panel": [
        r"add_methodology_panel\s*\(",
        r"methodology_text",
    ],
    "uses_cohens_d": [
        r"cohens_d",
        r"cohen",
        r"effect_size",
    ],
    "uses_canonical_palettes": [
        r"from\s+mousedb\.figures\.palettes",
        r"COHORT_COLORS",
        r"PHASE_COLORS",
        r"PELLET_PHASE_COLORS",
        r"OUTCOME_COLORS",
    ],
    "uses_inline_colors": [
        r"""['"]#[0-9a-fA-F]{6}['"]""",
    ],
    "is_figure_recipe": [
        r"class\s+\w+\(FigureRecipe\)",
        r"FigureRecipe",
    ],
    "uses_provenance_footer": [
        r"add_provenance_footer\s*\(",
    ],
    "uses_stat_bracket": [
        r"add_stat_bracket",
    ],
    "uses_validation": [
        r"validate_layout\s*\(",
        r"check_readability\s*\(",
    ],
}


def _check_patterns(source: str, patterns: List[str]) -> bool:
    """Check if any pattern matches in source."""
    for pat in patterns:
        if re.search(pat, source):
            return True
    return False


def _count_inline_colors(source: str) -> int:
    """Count inline hex color definitions (potential palette violations)."""
    return len(re.findall(r"""['"]#[0-9a-fA-F]{6}['"]""", source))


def _count_savefig_calls(source: str) -> int:
    """Count direct plt.savefig / fig.savefig calls (should use save_figure)."""
    return len(re.findall(r"\.savefig\s*\(", source))


def _count_figure_functions(source: str) -> int:
    """Estimate the number of distinct figures generated by a script."""
    # Count save_figure calls or savefig calls
    save_calls = len(re.findall(r"save_figure\s*\(", source))
    savefig_calls = len(re.findall(r"\.savefig\s*\(", source))
    return max(save_calls, savefig_calls, 1)


def audit_script(script_path: Path) -> Dict:
    """Audit a single figure generation script for compliance.

    Returns a dict with compliance flags and details.
    """
    source = script_path.read_text(encoding="utf-8", errors="replace")

    result = {
        "path": str(script_path),
        "name": script_path.name,
        "size_kb": round(script_path.stat().st_size / 1024, 1),
        "est_figures": _count_figure_functions(source),
    }

    # Check each compliance pattern
    for key, patterns in COMPLIANCE_PATTERNS.items():
        result[key] = _check_patterns(source, patterns)

    # Additional metrics
    result["inline_color_count"] = _count_inline_colors(source)
    result["direct_savefig_count"] = _count_savefig_calls(source)

    # Compute compliance score (out of 10 key checks)
    checks = [
        result.get("imports_mousedb_figures", False),
        result.get("uses_save_figure", False),
        result.get("uses_register", False),
        result.get("uses_figure_legend", False),
        result.get("uses_methodology_panel", False),
        result.get("uses_cohens_d", False),
        result.get("uses_canonical_palettes", False),
        not result.get("uses_inline_colors", True),  # inverted: no inline = good
        result.get("is_figure_recipe", False),
        result.get("uses_provenance_footer", False),
    ]
    result["compliance_score"] = sum(checks)
    result["compliance_max"] = len(checks)

    return result


