# Current active theme (set by apply_style)
_active_theme = "light"


def get_theme(theme=None):
    """Get theme colors dict. Uses active theme if none specified."""
    return THEMES[theme or _active_theme]


# =============================================================================
# Matplotlib rcParams presets
# =============================================================================

def _build_base_rcparams(theme="light"):
    """Build base rcParams for a given theme."""
    t = THEMES[theme]
    return {
        "figure.facecolor": t["background"],
        "axes.facecolor": t["background"],
        "savefig.facecolor": t["background"],
        "text.color": t["text"],
        "axes.labelcolor": t["text"],
        "xtick.color": t["text"],
        "ytick.color": t["text"],
        "font.family": "sans-serif",
        "font.sans-serif": ["Segoe UI", "Arial", "Helvetica", "DejaVu Sans"],
        "axes.spines.top": False,
        "axes.spines.right": False,
        "axes.edgecolor": t["spine"],
        "axes.linewidth": 1.0,
        "axes.grid": False,
        "grid.alpha": 0.15,
        "grid.linewidth": 0.5,
        "grid.color": t["grid"],
        "legend.framealpha": 0.85,
        "legend.edgecolor": t["legend_border"],
        "legend.facecolor": t["legend_bg"],
    }


def _build_publication_rcparams(theme="light"):
    return {
        **_build_base_rcparams(theme),
        "font.size": 10,
        "axes.titlesize": 11,
        "axes.labelsize": 10,
        "xtick.labelsize": 9,
        "ytick.labelsize": 9,
        "legend.fontsize": 9,
        "figure.dpi": 300,
        "savefig.dpi": 300,
    }


def _build_presentation_rcparams(theme="light"):
    return {
        **_build_base_rcparams(theme),
        "font.size": 12,
        "axes.titlesize": 16,
        "axes.labelsize": 13,
        "xtick.labelsize": 11,
        "ytick.labelsize": 11,
        "legend.fontsize": 10,
        "figure.dpi": 200,
        "savefig.dpi": 200,
    }


# Pre-built for backward compatibility (light theme)
_BASE_RCPARAMS = _build_base_rcparams("light")
RCPARAMS_PUBLICATION = _build_publication_rcparams("light")
RCPARAMS_PRESENTATION = _build_presentation_rcparams("light")


def apply_style(mode="presentation", theme="light"):
    """Apply Connectome figure style to matplotlib.

    Parameters
    ----------
    mode : str
        One of "publication", "presentation". Controls font sizes and DPI.
    theme : str
        One of "light", "dark", "print". Controls colors and backgrounds.
    """
    global _active_theme
    _active_theme = theme

    if mode == "publication":
        params = _build_publication_rcparams(theme)
    elif mode == "presentation":
        params = _build_presentation_rcparams(theme)
    else:
        raise ValueError(f"Unknown style mode: {mode!r}. Use 'publication' or 'presentation'.")

    plt.rcParams.update(params)
