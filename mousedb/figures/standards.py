"""
Figure generation standards for the Connectome project.

THE RULES. These are enforced, not optional. Every figure generated by any
Connectome tool must comply with these standards.

Rules:
    1. Every figure MUST have a methodology panel explaining what it shows.
    2. Every figure MUST have a provenance line (script, data, timestamp).
    3. Every figure MUST state N (sample size).
    4. Every figure with statistics MUST state: test name, p-values,
       sample sizes, and directionality.
    5. Every figure with phases MUST define phases in the methodology panel.
    6. Every figure with inclusion/exclusion criteria MUST state them.
    7. Color palettes from mousedb.figures.palettes ONLY -- no inline colors.
    8. No top or right spines.
    9. ASCII only in print/logging output (Windows cp1252 compatibility).
   10. Theme (light/dark/print) is a parameter, not hardcoded. Light is default
       for publication/posters; dark is available for presentations.
"""

